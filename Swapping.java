public class Swapping {
    public static void main(String[]args){
        int a=5;
        int b=9;
        a=a+b;
        b=a-b;
        a=a-b;
        System.out.println(a);
        System.out.println(b);
        int c=5;
        int d=6;
        c=c*d;
        d=c/d;
        c=c/d;
        System.out.println(c);
        System.out.println(d);
    }
    
}
